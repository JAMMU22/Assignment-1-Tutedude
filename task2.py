#Create a Personalized Greeting
#take a input from user as first name and last name
#and display a personalized greeting message.
# Step 1: Get first and last name from the user 
#using input function
first_name = input("Enter your first name: ")
last_name = input("Enter your last name: ")
#step 2:concatenate first and last name using + operator get full name
full_name = first_name + " " + last_name
#step 3:display a personalized greeting message
print(f"Hello, {full_name}! Welcome to our tutedude learning platform.")

